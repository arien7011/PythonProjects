#string functions
sentence = 'Hello this is arian here'

#len()
print(len(sentence))

#endswith() with endswith you can know or confirm the last word or character of string
print(sentence.endswith('here'))

#count() with count you can count the occurence of any word or characters in the string
print(sentence.count('a'))

#capitalise() it capitalise the first character of the string
print(sentence.capitalize())

#find() to find any word and character in the string
print(sentence.find('arian'))

#replace replace word or any character in the whole string irrespective of number of occurences
print(sentence.replace('a' ,'b'))